# Calculate Area of a Rectangle

'''
    Write a Python program that calculates the area of a rectangle given its length and its width.
'''

# Area of rectangle formula: area = length * width
length = int(input("Enter your length: "))
width = int(input("Enter your width: "))
area = length * width
print(f"The area of your rectangle is: {area}\N{SUPERSCRIPT TWO}")
